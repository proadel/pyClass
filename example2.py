# Example 2 | simple Calculator via PYTHON
#==========================================
num1 = int (input ("Enter First Number: ")) 
op = input ("Enter Operator: ")
num2 = int (input ("Enter Second Number: "))
if op == "+":
    print (num1 + num2)
if op == "-":
    print (num1 - num2)
if op == "*":
    print (num1 * num2)
if op == "/":
    print (num1 / num2)
#END---------------------------------------
#==========================================
# op = Operators = + - / * % ... etc. 
# num = Number
